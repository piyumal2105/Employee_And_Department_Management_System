import mongoose from "mongoose";

const hrManagerSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const HrManager = mongoose.model("HrManager", hrManagerSchema);

export default HrManager;
